package com.sz.qzxkj.design.observer.model;
/* 观察者模式
观察者模式定义了对象之间的一对多依赖，这样一来，当一个对象改变状态时，它的所有依赖者都会收到通知并自动更新。

发布 — 订阅
观察者模式有一个别名叫 “ 订阅 — 发布模式 ” 。报纸大家都订过吧，当你订阅了一份报纸，
每天都会有一份最新的报纸送到你手上，有多少人订阅报纸，报社就会发多少份报纸，
这是典型的订阅 — 发布模式，报社和订报纸的客户就是上面文章开头所说的 “ 一对多 ” 的依赖关系。






















*/